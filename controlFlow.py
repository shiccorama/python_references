### control flow --- if conditions ###

age = 18

if age > 18 :
	print("pass")
elif age < 18 :
	print("get out of here")
else :
	print("just get it")


# ternary if operator :

age = 18
print("pass" if age >= 18 else "get out of here")

# membership operators ( in && not it) :

friends = ["ali", "sara", "hassan"]
print("osama" in friends)  # False
print("sara" in friends)  # True
print("hassan" not in friends) # False, because "hassan" is in friends

